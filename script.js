    пересчитатьКачество(0, состояние.текущееСобытие.контекст);
    нормализовать();
    отрисовать(э);

    if (э.message) э.message.innerHTML = `<strong>${состояние.текущееСобытие.категория}.</strong> ${состояние.текущееСобытие.текст}`;
